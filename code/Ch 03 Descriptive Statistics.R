mean(x = apt$거래금액, trim = 0.10)


# ----------------------------------------------------------------------------
# 중위수
# ----------------------------------------------------------------------------

# 거래금액의 중위수를 확인합니다.
median(x = apt$거래금액)


# ----------------------------------------------------------------------------
# 분위수
# ----------------------------------------------------------------------------

# 거래금액의 백분위수를 확인합니다.
quantile(x = apt$거래금액, probs = seq(from = 0, to = 1, by = 0.01))

# 거래금액의 십분위수를 확인합니다.
quantile(x = apt$거래금액, probs = seq(from = 0, to = 1, by = 0.10))

# 거래금액의 사분위수를 확인합니다.
quantile(x = apt$거래금액)


# ----------------------------------------------------------------------------
# 범위와 사분범위
# ----------------------------------------------------------------------------

# 거래금액의 범위(최댓값과 최솟값의 간격)를 확인합니다.
max(x = apt$거래금액) - min(x = apt$거래금액)
range(apt$거래금액) %>% diff()

